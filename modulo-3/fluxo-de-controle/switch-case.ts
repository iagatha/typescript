let flor = 'tulipa';

switch (flor) {
    case 'rosa':
        console.log('Rosa são vermelhas');
    break;
    case 'violeta':
        console.log('Violeta são azuis');
    break;
    case 'tulipa':
        console.log('Tulipa são brancas');
    break;
    
    default:
        console.log('Opção inválida, selecione outra flor');
}